function updateDyeData() {
  const dataPath = path.join(__dirname, 'static', 'data', 'dyes.json');

  try {
    // JSONファイルを読み込み
    const data = JSON.parse(fs.readFileSync(dataPath, 'utf8'));

    // 各カララントの入手方法を更新
    data.dyes.forEach((dye) => {
      if (obtainMethods[dye.name]) {
        dye.obtainMethod = obtainMethods[dye.name];
      }
    });

    // 更新されたデータを保存
    fs.writeFileSync(dataPath, JSON.stringify(data, null, 2));
    console.log('カララントデータの入手方法を更新しました');
  } catch (error) {
    console.error('データ更新中にエラーが発生しました:', error);
  }
}

updateDyeData();
